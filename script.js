function setLang(lang) {
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (DATA[lang] && DATA[lang][key]) el.textContent = DATA[lang][key];
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    const active = btn.dataset.lang === lang;
    btn.classList.toggle('text-pitch', active);
    btn.classList.toggle('text-chalk/60', !active);
  });
  mountCards(lang);
  localStorage.setItem('mf1-lang', lang);
}

// Initialize Language Selection
setLang(localStorage.getItem('mf1-lang') || 'es');
document.querySelectorAll('.lang-btn').forEach(btn => btn.addEventListener('click', () => setLang(btn.dataset.lang)));

// Intersection Observer for scroll animation reveals
const observer = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('show');
    }
  });
}, { threshold: .08 });

// Observe reveal elements
function observeNewElements() {
  document.querySelectorAll('.reveal').forEach(el => observer.observe(el));
}
// Run once on load and after DOM injection
observeNewElements();

// Monitor changes in dynamically mounted grids to re-observe reveal classes
const config = { childList: true, subtree: true };
const callback = function(mutationsList) {
  for(let mutation of mutationsList) {
    if (mutation.type === 'childList') {
      observeNewElements();
    }
  }
};
const observerDOM = new MutationObserver(callback);
const mainApp = document.getElementById('top');
if (mainApp) {
  observerDOM.observe(mainApp, config);
}

// Track cursor movement for interactive background radial glows
